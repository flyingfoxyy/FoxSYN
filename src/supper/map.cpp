void *
mapper::create_abc_ntk_from_mapping(bool use_truth_table)
{
    Abc_Ntk_t *ntk = use_truth_table ? Abc_NtkAlloc(ABC_NTK_LOGIC, ABC_FUNC_SOP, 1) : Abc_NtkAlloc(ABC_NTK_LOGIC, ABC_FUNC_AIG, 1);
    std::vector<Abc_Obj_t *> idx2obj(num_nodes(), nullptr);

    ForEachGraphPi(*this) {
        auto pi = Abc_NtkCreatePi(ntk);
        Abc_ObjAssignName(pi, const_cast<char *>(get_pi_name(idx).c_str()), nullptr);
        idx2obj[_pi[idx]] = pi;
    }
    ForEachGraphPo(*this) {
        auto po = Abc_NtkCreatePo(ntk);
        Abc_ObjAssignName(po, const_cast<char *>(get_po_name(idx).c_str()), nullptr);
        idx2obj[_po[idx]] = po;
    }

    auto create_sop_node = [&](uint idx, Abc_Obj_t *pConst1) {
        const Cut *cut = best_cut(idx);
        word truth = compute_truth(cut, idx);
        Abc_Obj_t *pLut = Abc_NtkCreateObj(ntk, ABC_OBJ_NODE);
        if (truth == 0ul || truth == ~0ul) [[unlikely]] {
            Abc_ObjAddFanin(pLut, pConst1);
            if (truth == 0ul)
                pLut->pData = Abc_SopCreateBuf((Mem_Flex_t *)ntk->pManFunc);
            else
                pLut->pData = Abc_SopCreateInv((Mem_Flex_t *)ntk->pManFunc);
        } else {
            pLut->pData = Abc_SopRegister((Mem_Flex_t*)ntk->pManFunc, Abc_SopCreateFromTruth((Mem_Flex_t *)ntk->pManFunc,
                cut->size, (unsigned *)&truth));
            for (int m = 0; m != cut->size; ++m)
                Abc_ObjAddFanin(pLut, idx2obj[cut->begin()[m]]);
        }
        return pLut;
    };

    if (use_truth_table) {
        Abc_Obj_t *pConst1 = Abc_NtkCreateNodeConst1(ntk);
        // Hook the pwr
        idx2obj[pwr()] = pConst1;

        ForEachGraphLut(*this) {
            idx2obj[idx] = create_sop_node(idx, pConst1);
        }

        ForEachGraphPoV(*this) {
            const node_t &po = get_po(idx);
            if (po[0].sign()) {
                Abc_Obj_t *pInv = Abc_NtkCreateNodeInv(ntk, idx2obj[po[0].id()]);
                Abc_ObjAddFanin(idx2obj[po_id(idx)], pInv);
            } else {
                Abc_ObjAddFanin(idx2obj[po_id(idx)], idx2obj[po[0].id()]);
            }
        }

        // remove it if not used
        if (Abc_ObjFanoutNum(pConst1) == 0)
            Abc_NtkDeleteObj(pConst1);
    } else {

    }

    return static_cast<void *>(ntk);
}

void
mapper::create_simple_gates(uint max_size)
{
    mapper &mgr = *this;

    // mgr.to_dot("graph.dot");
    // std::system("dot -Tpdf graph.dot -o graph.pdf");
    // std::system("cp graph.pdf /mnt/c/Users/chang/Desktop/");
    // std::system("rm graph.dot");

    _timer.start("create_gate");
    _gates.resize(num_nodes(), nullptr);

    // find the gate roots at first
    std::fill(_gates.begin(), _gates.begin() + mgr.num_pi() + 1, (Gate *)01);
    ForEachGraphPoV(mgr) {
        _gates[n[0]] = (Gate *)01;
    }
    ForEachGraphLogicNodeRev(mgr) {
        if (mgr.num_ref(idx) > 1) {
            _gates[idx] = (Gate *)01;
        }
        const auto &n = mgr[idx];
        for (int i = 0; i != n.size(); ++i) {
            if (n[i].sign()) {
                _gates[n[i]] = (Gate *)01;
            }
        }
    }

    std::function<void(Lit, Array<Lit> &)> fn = [&](Lit id, Array<Lit> &inputs) {
        if (_gates[id]) {
            inputs.push_unique(id);
        } else {
            for (int i = 0; i != mgr[id].size(); ++i) {
                if (inputs.size() >= max_size)
                    break;
                fn(mgr[id][i], inputs);
            }
        }
    };

    // create simple gates
    ForEachGraphLogicNodeRev(*this) {
        if (_gates[idx] == (Gate *)01) {
            Array<Lit> inputs; inputs.reserve(max_size);
            for (int i = 0; i != mgr[idx].size(); ++i) {
                fn(mgr[idx][i], inputs);
            }
            for (Lit i : inputs) {
                assert(_gates[i] == (Gate *)01);
            }
            Gate *gate = new Gate(std::move(inputs));
            _gates[idx] = gate;
        }
    }

    int num_gate = 0;
    std::map<int, int> size2num;
    // int id = 0;
    for (Gate *gate : _gates) {
        if (gate && gate != (Gate *)01) {
            ++num_gate;
            ++size2num[gate->size()];
            // std::cout << id << "\n";
        }
        // ++id;
    }

    for (CREF [size, number] : size2num) {
        LOG(std::cout, "  gate size {} : {}", size, number);
    }

    _timer.stop("create_gate");
}

graph_t *
mapper::run_lut_mapping(const Config &cfg)
{
    _cfg     = cfg;
    _rank_fn = CutCost::GetRankFn(cfg.opt_target);

    // Setup PI cuts
    constexpr uint kTrivCutMemSize = Cut::bytes_needed<Cut::KCUT>(1);
    void *pi_triv_cuts = std::calloc(1, kTrivCutMemSize * num_pi());
    {
        char *p = reinterpret_cast<char *>(pi_triv_cuts);
        ForEachGraphPi(*this) {
            uint id         = _pi[idx];
            Cut *cut        = reinterpret_cast<Cut *>(p);
            cut->sign       = SIGNATURE(id);
            cut->size       = 1;
            cut->begin()[0] = id;
            _cuts[id].push_back(cut);
            p += kTrivCutMemSize;
        }
        Assert(p == (reinterpret_cast<char *>(pi_triv_cuts) + kTrivCutMemSize * num_pi()));
    }

    // Setup best cuts
    const uint kBestCutMemSize = Cut::bytes_needed<Cut::KCUT>(cfg.lut_size);
    Assert(_nodes[logic_begin()].is_logic());
    _best_cuts.set_offset(logic_begin());
    _best_cuts.resize(num_logic(), nullptr);
    ForEachGraphLogicNode(*this) {
        Cut *cut = (Cut *)std::calloc(1, kBestCutMemSize);
        cut->ms  = kBestCutMemSize;
        _best_cuts[idx] = cut;
    }

    // create simple gates boundry
    if (run_agdmap()) {
        create_simple_gates(8);
    }

    // according to run-time parameters, choose mapping algorithm

    int num_pass_flow  = 3;
    int num_pass_exact = 1;

    for (int i = 0; i != num_pass_flow; ++i) {
        MappingPass(CutCostAlgo::FLOW, *this, i);
    }

    // for (int i = 0; i != num_pass_exact; ++i) {
    //     MappingPass(CutCostAlgo::EXACT, *this, i);
    // }

    std::free(pi_triv_cuts);
    return create_mapped_graph();
}


Abc_Ntk_t *PerformSupperMap(Abc_Ntk_t *pNtk, const Config &cfg)
{
    TIME_START(ALL);

    ///////////////////////////////////////
    mapper *mgr = mapper::create_from_aig(static_cast<void *>(pNtk));
    ///////////////////////////////////////

    if (!mgr) [[unlikely]] {
        std::println(std::cout, "SuperMap: failed to create mapper.");
        return nullptr;
    }

    if (cfg.verbose) {
        std::println(std::cout, ">>> SuperMap Started");
        std::println(std::cout, "    graph info: PI = {}, PO = {}, LOGIC = {}", mgr->num_pi(), mgr->num_po(), mgr->num_logic());
    }

    mgr->timer().start("lut_mapping");
    ///////////////////////////////////////
    graph_t *g = mgr->run_lut_mapping(cfg);
    ///////////////////////////////////////
    mgr->timer().stop ("lut_mapping");

    mgr->timer().start("create_abc_ntk");
    ///////////////////////////////////////
    Abc_Ntk_t *res_ntk = static_cast<Abc_Ntk_t *>(g ? g->to_abc_ntk() : mgr->create_abc_ntk_from_mapping());
    ///////////////////////////////////////
    mgr->timer().stop ("create_abc_ntk");

    TIME_STOP(ALL);

    if (cfg.verbose) {
        std::println(std::cout, ">>> Runtime Report");
        std::println(std::cout, "  Total CPU  Time {}  ", Timer::formatted_time(cpu_ALL,  5));
        std::println(std::cout, "  Total Wall Time {}\n", Timer::formatted_time(wall_ALL, 5));
        mgr->timer().report(std::cout);
    }

    ///////////////////////////////////////
    delete g;
    delete mgr;
    ///////////////////////////////////////

    return res_ntk;
}

